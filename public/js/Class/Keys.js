/**************************************************
** GAME KEYBOARD CLASS
**************************************************/
class Keys {

  constructor(up, left, right, down) {
    this.up = up || false;
  	this.left = left || false;
  	this.right = right || false;
  	this.down = down || false;
  }

	onKeyDown(e) {
		var that = this,
			c = e.keyCode;
		switch (c) {
			// Controls
			case 37: // Left
				that.left = true;
				break;
			case 38: // Up
				that.up = true;
				break;
			case 39: // Right
				that.right = true; // Will take priority over the left key
				break;
			case 40: // Down
				that.down = true;
				break;
		}
	}

	onKeyUp(e) {
		var that = this,
			c = e.keyCode;
		switch (c) {
			case 37: // Left
				that.left = false;
				break;
			case 38: // Up
				that.up = false;
				break;
			case 39: // Right
				that.right = false;
				break;
			case 40: // Down
				that.down = false;
				break;
		}
	}
}
